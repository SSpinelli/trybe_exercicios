// Retorne a soma dos número pares usando apenas reduce;
const numbers = [18, 19, 23, 53, 4, 5, 76, 23, 54];

const sumOfEvens = (numbers) => {
  return numbers.reduce((accumulator, number) => {
    return (number % 2 === 0) ? accumulator + number : accumulator;
  }, 0);
}

console.log(sumOfEvens(numbers));

// Retorne a soma dos números pares usando reduce e filter;

const sumOfEvensWithFilter = (numbers) => {
  return numbers.filter((number) => number % 2 === 0).reduce((accumulator, evenNumber) => {
    return accumulator + evenNumber;
  }, 0)
}

console.log(sumOfEvensWithFilter(numbers))

